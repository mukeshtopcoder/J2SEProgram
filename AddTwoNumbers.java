import java.util.*;
class AddTwoNumbers{
	public static void main(String[] args){
		Scanner sc = new Scanner(System.in);
		int a,b,c;
		System.out.print("Enter A Numbers : ");
		a = sc.nextInt();
		System.out.print("Enter B Numbers : ");
		b = sc.nextInt();
		c = a+b;
		System.out.print("Addition : "+c);
	}
}